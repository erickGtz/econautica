    <section>
        <h2>¿Qué es el ODS 14?</h2>
        <p>
            El <strong>Objetivo 14</strong> busca <strong>conservar y utilizar sosteniblemente los océanos, mares y recursos marinos</strong> para el desarrollo sostenible. Los océanos son vitales para la humanidad, ya que producen más del <span class="highlight">50% del oxígeno</span> que respiramos, regulan el clima global y son fuente de alimentos y recursos económicos para millones de personas.
        </p>
        <p>
            Sin embargo, la contaminación, la sobrepesca y el cambio climático están amenazando estos ecosistemas. La protección de la vida submarina es crucial para garantizar un planeta saludable y próspero.
        </p>
    </section>

    <section>
        <h2>Contexto en México</h2>
        <p>
            México cuenta con una <strong>superficie marítima de 3.1 millones de km²</strong>, lo que equivale al 60% más que su territorio terrestre. Esta vasta riqueza marina incluye costas, mar profundo y zonas insulares que son hogar de una biodiversidad impresionante.
        </p>
        <p>
            La <strong>pesca</strong>, como actividad económica, se concentra principalmente en el <strong>Golfo de California</strong> y la costa occidental de la Península de Baja California, representando el <span class="highlight">75% de las capturas nacionales</span>. Además, Campeche y Yucatán contribuyen con el 15% de las capturas totales.
        </p>
        <p>
            México enfrenta el desafío de promover el uso sostenible de estos recursos a través de prácticas responsables como la <strong>gestión pesquera, la acuicultura sostenible y el turismo ecológico</strong>.
        </p>
    </section>

    <section>
        <h2>Propósito del Proyecto Econautica</h2>
        <p>
            <strong>Econautica</strong> es una plataforma dedicada a promover el <strong>turismo sostenible</strong> en México, con un enfoque en la <strong>vida submarina</strong>. Este proyecto busca:
        </p>
        <ul>
            <li>Informar sobre la importancia de la conservación de los océanos y su biodiversidad.</li>
            <li>Brindar un espacio para que los usuarios puedan descubrir y reservar actividades turísticas responsables.</li>
            <li>Mostrar estadísticas y datos sobre el impacto positivo del turismo sostenible en la economía local y la preservación del medio ambiente.</li>
            <li>Fomentar la educación ambiental y la concienciación sobre los beneficios de proteger los ecosistemas marinos.</li>
        </ul>
    </section>

    <section>
        <h2>¿Qué puedes hacer?</h2>
        <p>
            Tú también puedes contribuir al cuidado de los océanos a través de prácticas responsables, como:
        </p>
        <ul>
            <li>Participar en actividades turísticas sostenibles.</li>
            <li>Reducir el uso de plásticos para evitar la contaminación marina.</li>
            <li>Apoyar iniciativas que promuevan la conservación de los ecosistemas submarinos.</li>
        </ul>
        <p>
            Cada pequeña acción cuenta para garantizar un futuro sostenible para las generaciones venideras.
